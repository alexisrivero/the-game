# los principios de la orientacion a objetos:

# 1: encapsule siempre lo que varia
# 2: favorezca la composicion sobre la herencia
# 3: programe siempre a interfaces y no a implementaciones
# 4: siempre apunte a disenios lo mas desacoplados posibles entre objetos que interactuan
# 5: las clases deberian estar abiertas para extension pero cerradas para modificacion
# 6: depender de abstracciones, no depender de clases concretas
# 7: principio de responsabilidad unica: tiene que haber solo 1 razon para cambiar una clase
